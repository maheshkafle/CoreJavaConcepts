public class DataTypes {

        public static void main(String[] args) {
            // Interview Tips: Primitive Data Types: int, double, char and boolean but String is not a datatype but a class
            // 1. int
            int i = 1;
            i = 2;
            // int i = 0;
            System.out.println(i);

            // 2. double
            double k = 12.33D;
            double l = 14.55D;
            double m = 15.55D;
            double n = 100; // interpreted as 100.0
            System.out.println(n);

            // 3. char holds single character
            char a = 's';

            // 4. boolean
            boolean a1 = true;
            boolean b1 = false;
            System.out.println(b1);

            // String is not a datatype but a class. We can assign multiple character with String class
            String s = "This is multiline string value";
            String t = "12.44";
            System.out.println(s);
        }
}


